#include "libft.h"

char	*ft_strndup(const char *s, size_t len)
{
	char	*str;
	size_t	i;

	if (!(str = (char *)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	i = 0;
	while (i < len)
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
