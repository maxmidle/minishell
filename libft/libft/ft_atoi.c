#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int nb;
	int neg;

	neg = 1;
	i = 0;
	nb = 0;
	while (ft_issep(str[i]))
		i++;
	if ((str[i] == '-' || str[i] == '+') &&
			(str[i + 1] <= '9' && str[i + 1] >= '0'))
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - 48);
		i++;
	}
	return (nb * neg);
}
